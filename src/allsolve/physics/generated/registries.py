def _camel_to_snake(name: str) -> str:
    return re.sub(r"(?<!^)(?=[A-Z])", "_", name).lower()


def get_interaction_class(
    physics_definition: str, interaction_definition: str
) -> type[Interaction] | None:
    interactions = INTERACTIONS_BY_PHYSICS.get(physics_definition)
    if interactions is None:
        interactions = INTERACTIONS_BY_PHYSICS.get(_camel_to_snake(physics_definition))
    if interactions is None:
        return None
    return interactions.get(interaction_definition) or interactions.get(
        _camel_to_snake(interaction_definition)
    )


